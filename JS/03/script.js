let numberOne = Number(prompt("Digite o primeiro número: "))
let numberTwo = Number(prompt("Digite o segundo número: "))

alert(`Soma de ${numberOne} + ${numberTwo} = ${numberOne + numberTwo}`)
alert(`Subtração de ${numberOne} - ${numberTwo} = ${numberOne - numberTwo}`)
alert(`Multiplicação de ${numberOne} * ${numberTwo} = ${numberOne * numberTwo}`)
alert(`Divisão de ${numberOne} / ${numberTwo} = ${numberOne / numberTwo}`)
alert(`Resto da divisão de ${numberOne} por ${numberTwo} = ${numberOne % numberTwo}`)